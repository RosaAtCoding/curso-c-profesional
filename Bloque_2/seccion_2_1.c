/*
Sección 2.1 - Estructura básica de un programa en C

Objetivo: Entender las piezas mínimas de un prorama: includes y main()
*/

#include <stdio.h>  // Librería estándar de entrada/salida (printf, scanf, ...)

int main(void) {    // Punto de entrada del programa. "void" indica que no recibe argumentos

    printf("Esta es la estructura básica de un programa en C.\n");

    return 0;   // "0" normalmente significa "Exito" para el Sistema Operativo
}
